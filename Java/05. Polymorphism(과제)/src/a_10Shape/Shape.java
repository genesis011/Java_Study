package a_10Shape;

public class Shape {
	private int x,y;
	private double size;
	public int getX() {return x;};
	public int getY() {return y;};
	public double getSize() {return size;};
	
	public void setX() {int x;};
	public void setY() {int y;};
	public void setSize(double size) {this.size=size;};
	
	public double area() {
		return size;
	}
	public double circumference() {
		return size;
	}
}
