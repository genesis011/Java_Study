package exmple;

public class Exemple extends Exception{

	public Exemple() {
		super();
		
	}

	public Exemple(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public Exemple(String message, Throwable cause) {
		super(message, cause);
		
	}
                                                               
	public Exemple(String message) {
		super(message);
		
	}

	public Exemple(Throwable cause) {
		super(cause);
		
	}

	
}
